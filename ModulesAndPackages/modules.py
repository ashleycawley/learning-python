#!/usr/bin/env python3

first_name = 'Ashley'
last_name = 'Cawley'

# The conventional longer-hand way:
print('hello world, my first name is: ' + first_name + ' and my last name is: ' + last_name + '.')

# The faster, easier short-hand way with "f strings":
print(f'hello world, my first name is: {first_name} and my last name is: {last_name}.')

